// atribuição
let idade = 30
console.log(idade)

// soma
idade = 30 + 5
console.log(idade)

// subtração
idade = 30 - 5
console.log(idade)

console.log("A idade é " + idade) // + de concatenação

// multiplicação
let multiplicador = 4
let multiplicando = 2
let produto = multiplicador * multiplicando
console.log("Resultado da multiplicação é " + produto)

// divisão
let total = 50
let dividirPor = 2
console.log("Total da divisão: " + total / dividirPor)

// módulo / resto da divisão
let calculo = 11 % 3
console.log("Resto da divisão: " + calculo)

// ++ incremento
let pontos = 100
console.log("Pontos iniciais: " + pontos)
pontos++
console.log("Pontos após incremento: " + pontos)

// -- decremento
let vidas = 3
console.log("Vidas iniciais: " + vidas)
vidas--
console.log("Vidas após decremento: " + vidas)

// calcular e atribuir
