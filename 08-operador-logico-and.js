// AND ( && ) as condições precisam ser verdadeiras ou falsa

// Condição ser maior de 18 e ter visto verificado
let idade = 18
let vistoVerificado = true
let resultado = (idade >= 18) && (vistoVerificado === true)
console.log(resultado) // true

// Condição coletar 100 moedas E 1 item estrela
let moedasColetadas = 99
let item = "estrela"
let verificado = (moedasColetadas >= 100) && (item === "estrela")
console.log(verificado) // true

